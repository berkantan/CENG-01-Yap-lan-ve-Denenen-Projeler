        <h3>Basketbol</h3>
        <p> 
            Lise kariyeri için St Vincent-St. Mary Lisesi'ni seçen <b><i>LeBron James</i></b>, kısa sürede okulun üniversite basketbol takımına yükseldi. Lisedeki ilk yılında maç başına 21 sayı ve 6 ribaund ortalamaları ile oynadı. Takımıysa sezonu 27-0'lık bir dereceyle noktalayarak İrlandalı Savaş Bölümü III'ü yani eyalet şampiyonluğunu kazandı. Lisedeki ikinci sezonunda maç başına 25,2 sayı, 7,2 ribaund, 5,8 asist ve 3,8 top çalma ortalamaları ile oynadı. Ayrıca bu sezonda <b><i>LeBron James'in</i></b> takımıyla oynadığı bazı maçlarda Rodos Arena'ya bazı NBA takipçileri ve NBA scoutları onu izlemek için geldiler. Takımı bu yılı ise 26-1'lik derece ile noktalayarak İrlandalı Bölümünü şampiyon olarak noktaladı. Onun lisede ikinci yılındaki gösterdiği performansıyla Ohio Mr. Basketbol ve ABD Yılın En İyi Liseliler Beşi'ne seçildi, ayrıca lisedeki ikinci yılında Mr. Basketbol seçilen tarihteki ilk oyuncu oldu.

